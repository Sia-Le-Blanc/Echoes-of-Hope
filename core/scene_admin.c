            ClearScreen();
            printf("┌──────────────────────────────────────────────────────────────────┐\n");
            printf("│ 장비 설정 - 미구현                                               │\n");
            printf("│ 추후 data/equipment/*.txt 파일 직접 편집 가능                    │\n");
            printf("└──────────────────────────────────────────────────────────────────┘\n");
